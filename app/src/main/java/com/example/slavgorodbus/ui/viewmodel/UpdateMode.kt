package com.example.slavgorodbus.ui.viewmodel

/**
 * Режимы проверки обновлений
 * 
 * Используется в UI для отображения и управления
 * настройками автоматической проверки обновлений.
 */
enum class UpdateMode {
    /** Автоматическая проверка обновлений при запуске приложения */
    AUTOMATIC,
    
    /** Только ручная проверка обновлений по требованию пользователя */
    MANUAL,
    
    /** Полное отключение проверки обновлений */
    DISABLED
}
